unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	j;
	unsigned int	i;

	i = 0;
	j = 0;
	while (src[j] != '\0')
	{
		j++;
	}
	if (size != 0)
	{
		while (src[i] != '\0' && i < size -1)
		{
		dest[i] = src[i];
		i++;
		}
		dest[i] = '\0';
	}
	return (j);
}
